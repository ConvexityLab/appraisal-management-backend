/** Returns ISO timestamp N days before Feb 25 2026 */
function daysBack(n, hoursBack = 0) {
  const d = new Date('2026-02-25T12:00:00Z');
  d.setDate(d.getDate() - n);
  d.setHours(d.getHours() - hoursBack);
  return d.toISOString();
}

